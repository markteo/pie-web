package pie;

public class ResponseQuestion {

	private int responseQuestionID;
	private String responseQuestionText;
	
	public int getResponseQuestionID() {
		return responseQuestionID;
	}
	
	public void setResponseQuestionID(int responseQuestionID) {
		this.responseQuestionID = responseQuestionID;
	}
	
	public String getResponseQuestionText() {
		return responseQuestionText;
	}
	
	public void setResponseQuestionText(String responseQuestionText) {
		this.responseQuestionText = responseQuestionText;
	}
	
}
